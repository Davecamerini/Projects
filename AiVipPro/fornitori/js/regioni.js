// Funzione per aggiungere i poligoni delle regioni
function addRegionPolygons(map) {
    for (const [nomeRegione, coordinate] of Object.entries(regioni)) {
        const polygon = L.polygon(coordinate, { color: 'blue', fillOpacity: 0.5 }).addTo(map);
        polygon.bindPopup(nomeRegione);
    }
}
